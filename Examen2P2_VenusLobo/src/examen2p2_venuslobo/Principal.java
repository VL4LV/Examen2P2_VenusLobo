    private void tree_vehiculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tree_vehiculoMouseClicked
        // TODO add your handling code here:
        if (evt.isMetaDown()) {
             //seleccionar un nodo con click derecho
            int row = tree_vehiculo.getClosestRowForLocation(evt.getX(), evt.getY());
            tree_vehiculo.setSelectionRow(row);
            Object v1 = tree_vehiculo.getSelectionPath().getLastPathComponent();
            nodo_seleccionado = (DefaultMutableTreeNode) v1;
            
            if (nodo_seleccionado.getUserObject() instanceof vehiculo) {
                eleccionada= (vehiculo) nodo_seleccionado.getUserObject();
                pop_tree.show(evt.getComponent(), evt.getX(), evt.getY());
            }
        }
    }//GEN-LAST:event_tree_vehiculoMouseClicked

