    private void pop_agregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_pop_agregarActionPerformed
        // TODO add your handling code here:

        String anioTexto = texto_anio.getText();
        String marca = texto_marca.getText();
        String mdelo = texto_modelo.getText();
        String precioTexto = texto_precio.getText();
        Date creacion = date.getDate();

        //validar que todos los campos este llenos 
        if (anioTexto.isEmpty()
                || marca.isEmpty()
                || mdelo.isEmpty()
                || precioTexto.isEmpty()
                || creacion == null) {
            JOptionPane.showMessageDialog(this, "Llene todos los campos");
        } else {
            //hacer unos cambios con algunas variables 
            int anio = Integer.parseInt(anioTexto);
            double precio = Double.parseDouble(precioTexto);

            //crear el nuevo vehiculo 
            vehiculo nuevoVehiculo = new vehiculo(anio, marca, mdelo, precio, creacion);
            //agregar al arraylist
            vehiculos.add(nuevoVehiculo);

            JOptionPane.showMessageDialog(this, "Vehiculo agregado");

            //aregar al arbol 
            DefaultTreeModel modeloARBOL = (DefaultTreeModel) tree_vehiculo.getModel();
            DefaultMutableTreeNode raiz = (DefaultMutableTreeNode) modeloARBOL.getRoot();

            int ani;
            vehiculo modelo;

            ani = nuevoVehiculo.getAnio();
            modelo = nuevoVehiculo;

            DefaultMutableTreeNode nodoAnioExistente = null;
            for (int i = 0; i < raiz.getChildCount(); i++) {
                DefaultMutableTreeNode nodoAnio = (DefaultMutableTreeNode) raiz.getChildAt(i);
                if (nodoAnio.getUserObject().equals(ani)) {
                    nodoAnioExistente = nodoAnio;
                    break;
                }
            }

            if (nodoAnioExistente == null) {
                nodoAnioExistente = new DefaultMutableTreeNode(ani);
                raiz.add(nodoAnioExistente);
                modeloARBOL.reload();
            }

            //agregar el modelo al nodo 
            DefaultMutableTreeNode nodoModelo = new DefaultMutableTreeNode(modelo);
            nodoAnioExistente.add(nodoModelo);
            modeloARBOL.reload();

        }
    }//GEN-LAST:event_pop_agregarActionPerformed

